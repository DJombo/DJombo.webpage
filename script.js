function switchTheme() {
    var theme1 = document.getElementById('theme1');
    var theme2 = document.getElementById('theme2');
    if (theme1.disabled) {
        theme1.disabled = false;
        theme2.disabled = true;
    } else {
        theme1.disabled = true;
        theme2.disabled = false;
    }
}


